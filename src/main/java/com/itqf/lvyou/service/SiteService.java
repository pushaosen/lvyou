package com.itqf.lvyou.service;

import java.util.List;

import com.itqf.lvyou.model.Site;

/**
 * 景点service业务逻辑处理
 * @author Administrator
 *
 */
public interface SiteService {

	/**
	 * 获取所有景点
	 * @return
	 */
	public abstract List<Site> getAllSite();

	/**
	 * 根据id去查询所有的景点
	 * @param string
	 * @return
	 */
	public abstract List<Site> getSiteBySceneId(String id);

	/**
	 * 创建景点
	 * @param sites
	 */
	public abstract void createSite(Site site);

}
